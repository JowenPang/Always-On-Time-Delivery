public class Depot {
    int numOfCustomers;
    int maxCapacity;
    int xCoordinate;
    int yCoordinate;

    public Depot(int numOfCustomers, int maxCapacity, int xCoordinate, int yCoordinate) {
        this.numOfCustomers = numOfCustomers;
        this.maxCapacity = maxCapacity;
        this.xCoordinate = xCoordinate;
        this.yCoordinate = yCoordinate;
    }
}